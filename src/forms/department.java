    private void search() {
        String txtSer;
        String query = "SELECT * FROM `department_data` WHERE ";
        if (radID.isSelected()) {
            txtSer = Tools.getnum(txtsearch.getText());
            //Tools.msg(txtSer);
            query += "Department_ID ";
            
            
        } else if (radname.isSelected()) {
            txtSer = Tools.removenum(txtsearch.getText());
            query += "Department ";
        }
        
        else {
            txtSer = Tools.removenum(txtsearch.getText());
            query += "Location ";
        }
        query += "like '%"+txtSer+"%'";
        DP.getCustom(query, tbl);
    }
    
