    private void tblMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblMouseClicked
        // TODO add your handling code here:
        try {
            int row = tbl.getSelectedRow();
            String ID = tbl.getValueAt(row, 0).toString();
            String name = tbl.getValueAt(row, 1).toString();
            String loc = tbl.getValueAt(row, 2).toString();

            txtID.setText(ID);
            txtname.setText(name);
            txtloc.setText(loc);

            add.setEnabled(false);
            up.setEnabled(true);
            del.setEnabled(true);
        } catch (Exception ex) {
            Tools.msg(ex.getMessage());
        }
    }//GEN-LAST:event_tblMouseClicked

    private void add() {
        sets();
        DP.add();
        clear();
        DP.selectAll(tbl);
    }

    private void delete() {
        if (Tools.ConfirmMsg("Are You Sure You Want To Delete this Department ?")) {
            sets();
            DP.delete();
            clear();
            DP.selectAll(tbl);
        }
    }

    private void update() {
        sets();
        DP.update();
        clear();
        DP.selectAll(tbl);
    }

    private void txtlocKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtlocKeyPressed
        // TODO add your handling code here:
        if (add.isEnabled()) {
            if (evt.getKeyCode() == KeyEvent.VK_ENTER) {
                add();
            }
        } else {
            if (evt.getKeyCode() == KeyEvent.VK_ENTER) {
                update();

            } else if (evt.getKeyCode() == KeyEvent.VK_DELETE) {
                delete();
            }
        }
    }//GEN-LAST:event_txtlocKeyPressed

    private void tblKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_tblKeyPressed
        // TODO add your handling code here:
        if (!add.isEnabled()) {
            if (evt.getKeyCode() == KeyEvent.VK_DELETE) {
                if (Tools.ConfirmMsg("Are You Sure You Want To Delete this Department ?")) {
                    sets();
                    DP.delete();
                    clear();
                    DP.selectAll(tbl);
                }
            }
        }
    }//GEN-LAST:event_tblKeyPressed

