    private String mylike(){
        return " like '%"+txtsearch.getText()+"%'";
    }
    private void search (){
        String test; 
        String searchquery = "select * from employee_data where ";
        if (radID.isSelected()) {
            test = Tools.getnum(txtsearch.getText());
            searchquery+= "Employee_ID"+mylike();
        } else if (radname.isSelected()) {
            test = Tools.removenum(txtsearch.getText());
            searchquery += "Name"+mylike();
        } else if (radadd.isSelected()) {
            searchquery += "Address"+mylike();
        } else if (radsalary.isSelected()) {
            test = Tools.removenum(txtsearch.getText());
            searchquery += "Salary"+mylike();
        } else if (radhire.isSelected()) {
            test = Tools.getnum(txtsearch.getText());
            searchquery += "Hire_Date"+mylike();
        } else if (radbirth.isSelected()) {
            test = Tools.getnum(txtsearch.getText());
            searchquery += "Birthday_Date"+mylike();
        } else if (radDep.isSelected()){
            searchquery += "Department_ID"+mylike()+" or Department"+mylike();
        } else {
            String str = ep.getEmpByPhone(txtsearch.getText());
            searchquery += "Employee_ID like '%"+str+"%'";
        }
        emp.getCustom(searchquery, tbl);
        if (tbl.getRowCount()<=0) {
                Tools.msg("Can't Find any information");
                emp.selectAll(tbl);
                txtsearch.setText("");
            }
    }
