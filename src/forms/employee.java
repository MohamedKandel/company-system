    private void sets() {
        SimpleDateFormat f = new SimpleDateFormat("yyyy-MM-dd");            //format of date
        String Hire = String.valueOf(f.format(hire.getDate()));
        String Birth = String.valueOf(f.format(birth.getDate()));
        String DName = cmx.getSelectedItem().toString();
        String DNO = dep.DP.getValueByName(DName);

        emp.setEmpNO(Integer.parseInt(empID.getText()));
        emp.setEmpName(empname.getText());
        emp.setAddress(empadd.getText());
        emp.setSalary(Double.parseDouble(empsalary.getText()));
        emp.setHiringDate(Hire);
        emp.setBirthdate(Birth);
        emp.setDeptNO(Integer.parseInt(DNO));
    }


