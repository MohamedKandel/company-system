    private void delete() {
        if (Tools.ConfirmMsg("Are You Sure You Want to Delete this Item ?")) {
            ep.setEmpNO(Integer.parseInt(empID.getText()));
            ep.deleteAllPhonesByEmp();
            
            sets();
            emp.delete();
            
            clear();
            emp.selectAll(tbl);
        }
    }
    Employee_Phones ep = new Employee_Phones();
    private void add() {
        sets();
        emp.add();
        
        //add phones here
        ep.setEmpNO(Integer.parseInt(empID.getText()));
        for (int i=0;i<tblPhones.getRowCount();i++) {
            ep.setPhone(tblPhones.getValueAt(i, 0).toString());
            ep.add();
        }
        clear();
        emp.selectAll(tbl);
    }
    private void update() {
        sets();
        emp.update();
        
        ep.setEmpNO(Integer.parseInt(empID.getText()));
        //delete all phones for this employee
        ep.deleteAllPhonesByEmp();
        //add phones again for this employee
        for (int i=0;i<tblPhones.getRowCount();i++) {
            ep.setPhone(tblPhones.getValueAt(i, 0).toString());
            ep.add();
        }
        
        clear();
        emp.selectAll(tbl);
    }
