    private void tblMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblMouseClicked
        // TODO add your handling code here:
        try {
            SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
            int row = tbl.getSelectedRow();
            empID.setText(tbl.getValueAt(row, 0).toString());
            empname.setText(tbl.getValueAt(row, 1).toString());
            empadd.setText(tbl.getValueAt(row, 2).toString());
            empsalary.setText(tbl.getValueAt(row, 3).toString());
            Date hdate = sdf.parse(tbl.getValueAt(row, 4).toString());
            Date bdate = sdf.parse(tbl.getValueAt(row, 5).toString());
            hire.setDate(hdate);
            birth.setDate(bdate);
            cmx.setSelectedItem(tbl.getValueAt(row, 7));

            ep.setEmpNO(Integer.parseInt(empID.getText()));
            ep.selectAll(tblPhones);
            
            add.setEnabled(false);
            up.setEnabled(true);
            del.setEnabled(true);
        } catch (Exception ex) {
            Tools.msg(ex.getMessage());
        }
    }//GEN-LAST:event_tblMouseClicked

    private void addDeptActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addDeptActionPerformed
        // TODO add your handling code here:'
        int DepNO = Integer.parseInt(dep.DP.getAutoNumber());
        String DepName = (String) Tools.InputBox("Enter Department Name :");
        String DepLoc = (String) Tools.InputBox("Enter Department Location :");
        
        if ("".equals(DepName) || "".equals(DepLoc)) {
            Tools.msg("Can't Save Department as value null");
        }
        else {
        dep.DP.setDeptNO(DepNO);
        dep.DP.setDeptName(DepName);
        dep.DP.setLocation(DepLoc);
        dep.DP.add();
        db.database.FillCombo("department", "DeptName", cmx);
        }
    }//GEN-LAST:event_addDeptActionPerformed

